def _payload_schema(dataset_id: str) -> PayloadSchemaRecord:
    required = list(_PAYLOAD_FIELDS[dataset_id])
    schema = {
        "type": "object",
        "additionalProperties": False,
        "required": required,
        "properties": {name: {"type": "string"} for name in required},
    }
    return PayloadSchemaRecord(
        _PAYLOAD_SCHEMA_IDS[dataset_id],
        dataset_id.removeprefix("dataset:") + "-record",
        schema,
        sha256(canonical_bytes(schema)),
    )


def _item(
    dataset_id: str,
    right_id: str,
    source: SourceRecordRecord,
    *,
    label: str,
    entity_id: str | None,
    content_revision: int = 1,
    revision_of: str | None = None,
) -> EvidenceItemRecord:
    payload = _payload(
        dataset_id,
        source,
        label=label,
        entity_id=entity_id,
        content_revision=content_revision,
    )
    public = _AVAILABILITY[dataset_id] == "public-publication"
    licensed = _AVAILABILITY[dataset_id] == "licensed-receipt"
    received = _TIMES[min(content_revision - 1, 2)]
    if source.source_record_key in {
        "x3-source-0003",
        "x3-source-0004",
        "x3-source-0026",
        "x3-source-0029",
    }:
        received = _TIMES[2]
    elif source.source_record_key == "x3-source-0034":
        received = datetime(2024, 10, 15, tzinfo=UTC)
    return with_machine_id(
        "evidence",
        EvidenceItemRecord(
            "",
            right_id,
            source.source_record_id,
            sha256(canonical_bytes(payload)),
            dataset_id.removeprefix("dataset:") + "-record",
            _PAYLOAD_SCHEMA_IDS[dataset_id],
            "interval",
            None,
            datetime(2024, 1, 1, tzinfo=UTC),
            None,
            date(2024, 1, 1),
            (
                datetime(2024, 9, 1, tzinfo=UTC)
                if source.source_record_key == "x3-source-0034"
                else (received if public else None)
            ),
            received if public or licensed else None,
            None if public else received,
            None,
            content_revision,
            revision_of,
            "published" if public else "received",
            _ACCESS[dataset_id],
            "x3-v1",
            "synthetic",
            "x3-research",
            payload,
            canonical_entity_type=entity_id.split(":", 1)[0] if entity_id else None,
            canonical_entity_id=entity_id,
        ),
    )


def _spans(item: EvidenceItemRecord) -> tuple[EvidenceSpanRecord, ...]:
    rendered = canonical_bytes(item.payload).decode()
    spans = []
    for field_name in sorted(item.payload):
        value = json.dumps(item.payload[field_name], separators=(",", ":"), ensure_ascii=True)
        marker = json.dumps(field_name) + ":"
        start = rendered.index(marker) + len(marker)
        end = start + len(value)
        spans.append(
            with_machine_id(
                "span",
                EvidenceSpanRecord(
                    "",
                    item.evidence_item_id,
                    f"/{field_name}",
                    start,
                    end,
                    sha256(rendered[start:end].encode()),
                ),
            )
        )
    return tuple(spans)


def _insert(conn, table: str, columns: tuple[str, ...], values: tuple[object, ...]) -> None:
    conn.execute(
        f"INSERT OR IGNORE INTO {table} ({','.join(columns)}) VALUES ({','.join('?' for _ in columns)})",
        values,
    )


def _item_for_version(
    source_items: tuple[EvidenceItemRecord, ...], version_index: int
) -> EvidenceItemRecord:
    if len(source_items) == 1:
        return source_items[0]
    if len(source_items) == 2:
        return source_items[1] if version_index >= 2 else source_items[0]
    return source_items[min(version_index, len(source_items) - 1)]


def _store_x3_composite(
    conn,
    *,
    policy: str,
    join_keys: tuple[str, ...],
    slice_digests: tuple[tuple[str, str], ...],
    rows: tuple[tuple[object, ...], ...],
    references: tuple[ReceiptReference, ...],
) -> tuple[str, str, str]:
    composite_digest = sha256(
        canonical_bytes(
            {
                "policy": policy,
                "join_keys": join_keys,
                "slice_digests": slice_digests,
                "rows": rows,
            }
        )
    )
    receipt = make_receipt(
        claim_id="claim:x3-composite-join",
        output_locator="/rows",
        input_digest=composite_digest,
        output_schema_id="schema:generic-v1",
        current_attestation="D",
        live_attestation_ceiling="B",
        algorithm_id=policy,
        algorithm_version="1",
        parameters={"join_keys": join_keys},
        value=rows,
        references=references,
    )
    store_receipt(conn, receipt)
    bundle_digest = digest_id(
        "bundle",
        {
            "policy": policy,
            "join_keys": join_keys,
            "slice_digests": slice_digests,
            "composite_digest": composite_digest,
            "join_receipt_id": receipt.receipt_id,
        },
    )
    conn.execute(
        "INSERT OR IGNORE INTO snapshot_bundle_manifest VALUES (?,?,?,?,?,?,?)",
        (
            bundle_digest,
            canonical_bytes({"policy": policy, "join_keys": join_keys}).decode(),
            canonical_bytes(slice_digests).decode(),
            composite_digest,
            receipt.receipt_id,
            len(rows),
            sha256(canonical_bytes(rows)),
        ),
    )
    return receipt.receipt_id, bundle_digest, composite_digest


def x3_manifest_payload(manifest: X3FixtureManifest) -> Mapping[str, object]:
    return MappingProxyType(
        {
            field.name: getattr(manifest, field.name)
            for field in fields(manifest)
            if field.name != "fixture_digest"
        }
    )


def x3_manifest_digest(manifest: X3FixtureManifest) -> str:
    return sha256(canonical_bytes(x3_manifest_payload(manifest)))


