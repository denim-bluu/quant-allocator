def _funnel_stage(opportunity_index: int, version_index: int) -> str:
    if version_index == 0:
        return "discovered"
    if version_index == 1:
        return ("contacted", "rfi_received", "screened")[opportunity_index % 3]
    if opportunity_index < 48:
        return "screened"
    if opportunity_index < 58:
        return "diligence"
    if opportunity_index < 65:
        return "approved"
    return ("discovered", "contacted", "rfi_received")[opportunity_index % 3]


def _funnel_status_reason(opportunity_index: int, stage: str) -> tuple[str, str]:
    if opportunity_index >= 78:
        side = _FUNNEL_SIDE_STATES[opportunity_index % len(_FUNNEL_SIDE_STATES)]
        reason = {
            "declined": "strategy-fit-failed",
            "manager_withdrew": "manager-withdrew",
            "unavailable": "capacity-unavailable",
            "paused": "capacity-unavailable",
            "duplicate": "duplicate-opportunity",
            "out_of_scope": "out-of-scope",
        }[side]
        return ("withdrawn" if side == "manager_withdrew" else "rejected", reason)
    reason_by_stage = dict(zip(_FUNNEL_STAGES, _FUNNEL_REASONS[:8], strict=True))
    return (
        "accepted" if stage in {"screened", "approved", "funded"} else "pending"
    ), reason_by_stage[stage]


