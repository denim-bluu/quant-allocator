def _persisted_receipt_is_valid(conn, receipt_id: str) -> bool:
    header = conn.execute(
        "SELECT * FROM reconstruction_receipt WHERE receipt_id=?", (receipt_id,)
    ).fetchone()
    if header is None:
        return False
    rows = conn.execute(
        "SELECT * FROM receipt_reference WHERE receipt_id=? ORDER BY ordinal", (receipt_id,)
    ).fetchall()
    if not rows or tuple(row["ordinal"] for row in rows) != tuple(range(len(rows))):
        return False
    references = []
    for row in rows:
        typed_column = _RECEIPT_REFERENCE_COLUMNS.get(row["reference_type"])
        if typed_column is None or row[typed_column] is None:
            return False
        if sum(row[column] is not None for column in _RECEIPT_REFERENCE_COLUMNS.values()) != 1:
            return False
        references.append(
            ReceiptReference(
                row["output_field"],
                row["reference_type"],
                row[typed_column],
                row["disposition"],
                row["reason_code"],
                row["source_schema_id"],
                row["source_field"],
                row["role"],
            )
        )
    references_tuple = tuple(references)
    seal = conn.execute("SELECT * FROM receipt_seal WHERE receipt_id=?", (receipt_id,)).fetchone()
    if (
        seal is None
        or seal["reference_count"] != len(references_tuple)
        or seal["references_sha256"] != sha256(canonical_bytes(references_tuple))
    ):
        return False
    expected_id = digest_id(
        "receipt",
        {
            "claim_id": header["claim_id"],
            "output_locator": header["output_locator"],
            "input_digest": header["input_digest"],
            "output_schema_id": header["output_schema_id"],
            "current_attestation": header["current_attestation"],
            "live_attestation_ceiling": header["live_attestation_ceiling"],
            "algorithm_id": header["algorithm_id"],
            "algorithm_version": header["algorithm_version"],
            "parameters_sha256": header["parameters_sha256"],
            "value_sha256": header["value_sha256"],
            "references": references_tuple,
        },
    )
    return expected_id == receipt_id


_X3_OWNED_RELATIONSHIP_ID_QUERY = (
    "SELECT r.entity_relationship_id FROM entity_relationship r "
    "JOIN evidence_item i ON i.evidence_item_id=r.source_evidence_item_id "
    "JOIN source_record s ON s.source_record_id=i.source_record_id "
    "WHERE s.dataset_id LIKE 'dataset:x3-%' ORDER BY r.entity_relationship_id"
)
_X3_OWNED_RELATIONSHIP_RECORD_QUERY = (
    "SELECT r.* FROM entity_relationship r "
    "JOIN evidence_item i ON i.evidence_item_id=r.source_evidence_item_id "
    "JOIN source_record s ON s.source_record_id=i.source_record_id "
    "WHERE s.dataset_id LIKE 'dataset:x3-%' ORDER BY r.entity_relationship_id"
)


def _x3_owned_relationship_ids(conn) -> tuple[str, ...]:
    return tuple(row[0] for row in conn.execute(_X3_OWNED_RELATIONSHIP_ID_QUERY))


def _x3_owned_relationship_records(conn) -> tuple[tuple[object, ...], ...]:
    return tuple(tuple(row) for row in conn.execute(_X3_OWNED_RELATIONSHIP_RECORD_QUERY))


def _authoritative_x3_inputs(conn):
    rights = {
        row[0]: row[1]
        for row in conn.execute(
            "SELECT dataset_id,evidence_right_id FROM evidence_right "
            "WHERE dataset_id LIKE 'dataset:x3-%' "
            "AND right_series_id!='right-series:x3-post-entitlement'"
        )
    }
    versions = tuple(
        row[0]
        for row in conn.execute(
            "SELECT dataset_version_id FROM dataset_version WHERE dataset_id LIKE 'dataset:x3-%' "
            "ORDER BY dataset_id,coalesce(published_at,first_observed_at_utc,received_at_utc),"
            "version_label"
        )
    )
    relationships = _x3_owned_relationship_ids(conn)
    return rights, versions, relationships


def _x3_projection_records(conn, table: str) -> tuple[tuple[object, ...], ...]:
    if table == "entity_relationship":
        return _x3_owned_relationship_records(conn)
    identifier = f"{table}_id"
    predicate = "source_key LIKE 'x3-source-%'"
    return tuple(
        tuple(row)
        for row in conn.execute(f"SELECT * FROM {table} WHERE {predicate} ORDER BY {identifier}")
    )


def _x3_canonical_taxonomy(conn) -> dict[str, tuple[str, str]]:
    expected = {
        **{
            f"manager:x3-{index:02d}": ("manager", f"Quill House {index + 1:02d}")
            for index in range(8)
        },
        **{
            f"adviser:x3-{index:02d}": ("adviser", f"Quill Advisory {index + 1:02d}")
            for index in range(8)
        },
        **{
            f"legal-entity:x3-{index:02d}": (
                "legal-entity",
                f"Quill Legal {index + 1:02d}",
            )
            for index in range(8)
        },
        **{
            f"strategy:x3-{index:02d}": ("strategy", f"Quill Strategy {index + 1:02d}")
            for index in range(24)
        },
        **{
            f"composite:x3-{index:02d}": (
                "composite",
                f"Quill Composite {index + 1:02d}",
            )
            for index in range(24)
        },
        **{
            f"vehicle:x3-{index:02d}": ("vehicle", f"Quill Vehicle {index + 1:02d}")
            for index in range(24)
        },
    }
    rows = {
        row["entity_id"]: (row["entity_type"], row["canonical_name"])
        for row in conn.execute(
            "SELECT entity_id,entity_type,canonical_name FROM canonical_entity "
            "WHERE entity_id LIKE '%:x3-%'"
        )
    }
    if rows != expected:
        raise ValueError("x3-canonical-taxonomy-mismatch")
    return rows


def _x3_source_target(source_key: str, dataset_id: str) -> tuple[str, str]:
    prefix = "x3-source-"
    if not source_key.startswith(prefix) or not source_key.removeprefix(prefix).isdigit():
        raise ValueError("x3-source-key-mismatch")
    index = int(source_key.removeprefix(prefix))
    if index >= 840 or dataset_id != _COVERAGE_DATASETS[index % 5]:
        raise ValueError("x3-source-dataset-mismatch")
    if dataset_id == "dataset:x3-public-adviser":
        return "manager", f"manager:x3-{index % 8:02d}"
    if dataset_id in {"dataset:x3-registered-fund", "dataset:x3-holdings-filer"}:
        return "vehicle", f"vehicle:x3-{index % 24:02d}"
    if dataset_id == "dataset:x3-strategy-export":
        return "composite", f"composite:x3-{index % 24:02d}"
    return "strategy", f"strategy:x3-{(index // 5) % 24:02d}"


def _x3_source_key_span(
    conn, item, payload: Mapping[str, object], source_key: str, cache: dict[str, str]
) -> str:
    item_id = item["evidence_item_id"]
    if item_id in cache:
        return cache[item_id]
    if (
        item["field_dictionary_version"] != "x3-v1"
        or item["payload_schema_id"] != _PAYLOAD_SCHEMA_IDS[item["dataset_id"]]
        or payload.get("source_key") != source_key
    ):
        raise ValueError("x3-source-field-mismatch")
    spans = conn.execute(
        "SELECT * FROM evidence_span WHERE evidence_item_id=? AND json_pointer='/source_key'",
        (item_id,),
    ).fetchall()
    if len(spans) != 1:
        raise ValueError("x3-source-span-mismatch")
    rendered = canonical_bytes(payload).decode()
    marker = json.dumps("source_key") + ":"
    start = rendered.index(marker) + len(marker)
    value = json.dumps(source_key, separators=(",", ":"), ensure_ascii=True)
    end = start + len(value)
    digest = sha256(rendered[start:end].encode())
    expected_id = machine_id(
        "span",
        {
            "evidence_item_id": item_id,
            "json_pointer": "/source_key",
            "start_char": start,
            "end_char": end,
            "span_sha256": digest,
        },
    )
    span = spans[0]
    if tuple(span) != (expected_id, item_id, "/source_key", start, end, digest):
        raise ValueError("x3-source-span-mismatch")
    cache[item_id] = expected_id
    return expected_id


def _x3_version_ids(conn, dataset_id: str) -> tuple[str, ...]:
    return tuple(
        row[0]
        for row in conn.execute(
            "SELECT dataset_version_id FROM dataset_version WHERE dataset_id=? "
            "ORDER BY coalesce(published_at,first_observed_at_utc,received_at_utc),version_label",
            (dataset_id,),
        )
    )


def _x3_projection_observations(conn, dataset_id: str, version_id: str):
    rows = conn.execute(
        "SELECT o.dataset_observation_id,o.observation_status,i.evidence_item_id,"
        "i.payload_json,i.payload_schema_id,i.field_dictionary_version,i.canonical_entity_id,"
        "i.version AS item_version,s.source_record_id,s.source_record_key,s.source_entity_type,"
        "s.dataset_id FROM dataset_observation o JOIN evidence_item i USING(evidence_item_id) "
        "JOIN source_record s USING(source_record_id) WHERE o.dataset_version_id=? "
        "AND s.dataset_id=? AND s.source_record_key LIKE 'x3-source-%' "
        "ORDER BY s.source_record_key,i.version",
        (version_id, dataset_id),
    ).fetchall()
    if len({row["source_record_key"] for row in rows}) != len(rows):
        raise ValueError("x3-projection-observation-duplicate")
    return rows


def _expected_x3_mapping_records(conn) -> tuple[tuple[object, ...], ...]:
    taxonomy = _x3_canonical_taxonomy(conn)
    span_cache: dict[str, str] = {}
    previous: dict[str, str] = {}
    projection_versions: dict[str, int] = {}
    records: list[tuple[object, ...]] = []
    for dataset_id in _COVERAGE_DATASETS[:5]:
        for version_index, version_id in enumerate(_x3_version_ids(conn, dataset_id)):
            for item in _x3_projection_observations(conn, dataset_id, version_id):
                source_key = item["source_record_key"]
                source_type, authored_target = _x3_source_target(source_key, dataset_id)
                ambiguous = source_key == "x3-source-0014"
                candidates_tuple = ("strategy:x3-02", "strategy:x3-03") if ambiguous else ()
                candidates = canonical_bytes(candidates_tuple).decode()
                canonical_target = None if ambiguous else authored_target
                label_entity = candidates_tuple[0] if ambiguous else authored_target
                if (
                    item["source_entity_type"] != source_type
                    or item["canonical_entity_id"] != canonical_target
                    or label_entity not in taxonomy
                    or taxonomy[label_entity][0] != source_type
                ):
                    raise ValueError("x3-mapping-target-mismatch")
                payload = json.loads(item["payload_json"])
                span_id = _x3_source_key_span(conn, item, payload, source_key, span_cache)
                source_ordinal = int(label_entity.rsplit("-", 1)[1])
                base_label = f"Quill {source_type.title()} {source_ordinal + 1:02d}"
                if dataset_id == "dataset:x3-public-adviser":
                    if payload.get("legal_name") != base_label:
                        raise ValueError("x3-mapping-label-mismatch")
                elif dataset_id == "dataset:x3-strategy-export":
                    if (
                        payload.get("product_id") != authored_target
                        or payload.get("product_name") != base_label
                    ):
                        raise ValueError("x3-mapping-payload-target-mismatch")
                elif dataset_id == "dataset:x3-rfi-ddq" and payload.get("strategy_id") != (
                    "unresolved" if ambiguous else authored_target
                ):
                    raise ValueError("x3-mapping-payload-target-mismatch")
                if not isinstance(base_label, str):
                    raise ValueError("x3-mapping-label-mismatch")
                source_label = (
                    "Quill Rebrand 02"
                    if source_key in {"x3-source-0009", "x3-source-0011"} and version_index == 2
                    else base_label
                )
                taxonomy_version = (
                    "x3-taxonomy-v2"
                    if source_key == "x3-source-0013" and version_index == 2
                    else _TAXONOMY
                )
                projection_version = projection_versions.get(source_key, 0) + 1
                projection_versions[source_key] = projection_version
                identity = {
                    "source_evidence_item_id": item["evidence_item_id"],
                    "source_key": source_key,
                    "source_label": source_label,
                    "taxonomy_version": taxonomy_version,
                    "version": projection_version,
                    "candidate_entity_ids_json": candidates,
                }
                mapping_id = machine_id("mapping", identity)
                records.append(
                    (
                        mapping_id,
                        item["evidence_item_id"],
                        span_id,
                        version_id,
                        item["dataset_observation_id"],
                        source_key,
                        source_label,
                        source_type,
                        canonical_target,
                        "ambiguous" if ambiguous else "resolved",
                        candidates,
                        "label-collision" if ambiguous else "authored-exact",
                        taxonomy_version,
                        "interval",
                        None,
                        "2024-01-01T00:00:00.000000Z",
                        None,
                        projection_version,
                        previous.get(source_key),
                    )
                )
                previous[source_key] = mapping_id
    return tuple(sorted(records))


def _expected_x3_relationship_records(conn) -> tuple[tuple[object, ...], ...]:
    taxonomy = _x3_canonical_taxonomy(conn)
    typed_entities = {
        entity_type: tuple(
            sorted(entity_id for entity_id, value in taxonomy.items() if value[0] == entity_type)
        )
        for entity_type in (
            "manager",
            "adviser",
            "legal-entity",
            "strategy",
            "composite",
            "vehicle",
        )
    }
    specs = [
        relation
        for manager, adviser, legal in zip(
            typed_entities["manager"],
            typed_entities["adviser"],
            typed_entities["legal-entity"],
            strict=True,
        )
        for relation in (("advised_by", manager, adviser), ("legal_identity", adviser, legal))
    ]
    specs.extend(
        relation
        for index, (strategy, composite, vehicle) in enumerate(
            zip(
                typed_entities["strategy"],
                typed_entities["composite"],
                typed_entities["vehicle"],
                strict=True,
            )
        )
        for relation in (
            ("offers", typed_entities["manager"][index // 3], strategy),
            ("reported_through", strategy, composite),
            ("implemented_by", composite, vehicle),
        )
    )
    dataset_id = _COVERAGE_DATASETS[0]
    version_ids = _x3_version_ids(conn, dataset_id)
    if len(version_ids) != 3:
        raise ValueError("x3-relationship-version-mismatch")
    early_by_source = {
        row["source_record_key"]: row
        for row in _x3_projection_observations(conn, dataset_id, version_ids[0])
    }
    latest_by_source = {
        row["source_record_key"]: row
        for row in _x3_projection_observations(conn, dataset_id, version_ids[-1])
    }
    source_keys = tuple(
        row[0]
        for row in conn.execute(
            "SELECT source_record_key FROM source_record WHERE dataset_id=? "
            "AND source_record_key LIKE 'x3-source-%' ORDER BY source_record_key",
            (dataset_id,),
        )
    )
    if len(source_keys) < len(specs):
        raise ValueError("x3-relationship-source-mismatch")
    span_cache: dict[str, str] = {}
    records: list[tuple[object, ...]] = []

    def expected_record(
        item,
        version_id: str,
        relation_type: str,
        source_entity: str,
        target_entity: str,
        effective_from: str,
        effective_to: str | None,
        relation_version: int,
        revision_of: str | None,
    ) -> tuple[object, ...]:
        source_key = item["source_record_key"]
        payload = json.loads(item["payload_json"])
        span_id = _x3_source_key_span(conn, item, payload, source_key, span_cache)
        if source_entity not in taxonomy or target_entity not in taxonomy:
            raise ValueError("x3-relationship-entity-mismatch")
        identity = {
            "source_evidence_item_id": item["evidence_item_id"],
            "relation_type": relation_type,
            "source_entity_id": source_entity,
            "target_entity_id": target_entity,
            "temporal_type": "interval",
            "effective_at": None,
            "effective_from": effective_from,
            "effective_to": effective_to,
            "version": relation_version,
        }
        relationship_id = machine_id("entity-relation", identity)
        return (
            relationship_id,
            item["evidence_item_id"],
            span_id,
            version_id,
            item["dataset_observation_id"],
            relation_type,
            source_entity,
            target_entity,
            "interval",
            None,
            effective_from,
            effective_to,
            relation_version,
            revision_of,
        )

    for ordinal, (relation_type, source_entity, target_entity) in enumerate(specs):
        source_key = source_keys[ordinal]
        latest = latest_by_source[source_key]
        if ordinal == 0:
            early = early_by_source[source_key]
            base = expected_record(
                early,
                version_ids[0],
                relation_type,
                source_entity,
                target_entity,
                "2024-01-01T00:00:00.000000Z",
                None,
                1,
                None,
            )
            records.append(base)
            records.append(
                expected_record(
                    latest,
                    version_ids[-1],
                    relation_type,
                    source_entity,
                    target_entity,
                    "2024-01-01T00:00:00.000000Z",
                    "2024-07-01T00:00:00.000000Z",
                    2,
                    str(base[0]),
                )
            )
            records.append(
                expected_record(
                    latest,
                    version_ids[-1],
                    relation_type,
                    source_entity,
                    typed_entities["adviser"][1],
                    "2024-07-01T00:00:00.000000Z",
                    None,
                    1,
                    None,
                )
            )
            continue
        records.append(
            expected_record(
                latest,
                version_ids[-1],
                relation_type,
                source_entity,
                target_entity,
                "2024-01-01T00:00:00.000000Z",
                None,
                1,
                None,
            )
        )
    return tuple(sorted(records))


def _projection_closure_is_valid(conn, manifest: X3FixtureManifest) -> bool:
    mappings = conn.execute(
        "SELECT * FROM entity_mapping WHERE source_key LIKE 'x3-source-%' "
        "ORDER BY entity_mapping_id"
    ).fetchall()
    relationships = conn.execute(_X3_OWNED_RELATIONSHIP_RECORD_QUERY).fetchall()
    expected_mappings = _expected_x3_mapping_records(conn)
    expected_relationships = _expected_x3_relationship_records(conn)
    if (
        tuple(tuple(row) for row in mappings) != expected_mappings
        or expected_mappings != manifest.mapping_records
        or tuple(tuple(row) for row in relationships) != expected_relationships
        or expected_relationships != manifest.relationship_records
        or tuple(row["entity_relationship_id"] for row in relationships)
        != manifest.relationship_ids
    ):
        return False

    valid_provenance = {
        tuple(row)
        for row in conn.execute(
            "SELECT i.evidence_item_id,sp.evidence_span_id,v.dataset_version_id,"
            "o.dataset_observation_id FROM evidence_item i "
            "JOIN source_record s USING(source_record_id) "
            "JOIN evidence_span sp USING(evidence_item_id) "
            "JOIN dataset_observation o USING(evidence_item_id) "
            "JOIN dataset_version v USING(dataset_version_id) "
            "WHERE s.dataset_id=v.dataset_id AND s.dataset_id LIKE 'dataset:x3-%'"
        )
    }
    canonical_entities = {
        row["entity_id"]: row["entity_type"]
        for row in conn.execute(
            "SELECT entity_id,entity_type FROM canonical_entity WHERE entity_id LIKE '%:x3-%'"
        )
    }
    source_keys = {
        row["source_record_key"]: row["source_entity_type"]
        for row in conn.execute(
            "SELECT source_record_key,source_entity_type FROM source_record "
            "WHERE dataset_id LIKE 'dataset:x3-%'"
        )
    }
    mapping_by_id = {row["entity_mapping_id"]: row for row in mappings}
    for row in mappings:
        identity = {
            "source_evidence_item_id": row["source_evidence_item_id"],
            "source_key": row["source_key"],
            "source_label": row["source_label"],
            "taxonomy_version": row["taxonomy_version"],
            "version": row["version"],
            "candidate_entity_ids_json": row["candidate_entity_ids_json"],
        }
        provenance = (
            row["source_evidence_item_id"],
            row["evidence_span_id"],
            row["dataset_version_id"],
            row["dataset_observation_id"],
        )
        try:
            candidates = json.loads(row["candidate_entity_ids_json"])
        except (TypeError, json.JSONDecodeError):
            return False
        resolved = row["mapping_status"] == "resolved"
        ambiguous = row["mapping_status"] == "ambiguous"
        if (
            row["entity_mapping_id"] != machine_id("mapping", identity)
            or provenance not in valid_provenance
            or row["source_key"] not in source_keys
            or row["source_entity_type"] != source_keys[row["source_key"]]
            or row["temporal_type"] != "interval"
            or row["effective_at"] is not None
            or row["effective_from"] is None
            or (row["effective_to"] is not None and row["effective_from"] >= row["effective_to"])
            or not isinstance(candidates, list)
            or not (resolved or ambiguous)
            or (resolved and (row["canonical_entity_id"] not in canonical_entities or candidates))
            or (resolved and row["resolution_rule"] != "authored-exact")
            or (ambiguous and (row["canonical_entity_id"] is not None or len(candidates) < 2))
            or (ambiguous and row["resolution_rule"] != "label-collision")
            or any(candidate not in canonical_entities for candidate in candidates)
        ):
            return False
        parent = mapping_by_id.get(row["revision_of"])
        if (row["version"] == 1 and row["revision_of"] is not None) or (
            row["version"] > 1
            and (
                parent is None
                or parent["version"] != row["version"] - 1
                or parent["source_evidence_item_id"] != row["source_evidence_item_id"]
                or parent["source_key"] != row["source_key"]
            )
        ):
            return False

    relationship_by_id = {row["entity_relationship_id"]: row for row in relationships}
    for row in relationships:
        identity = {
            "source_evidence_item_id": row["source_evidence_item_id"],
            "relation_type": row["relation_type"],
            "source_entity_id": row["source_entity_id"],
            "target_entity_id": row["target_entity_id"],
            "temporal_type": row["temporal_type"],
            "effective_at": row["effective_at"],
            "effective_from": row["effective_from"],
            "effective_to": row["effective_to"],
            "version": row["version"],
        }
        provenance = (
            row["source_evidence_item_id"],
            row["evidence_span_id"],
            row["dataset_version_id"],
            row["dataset_observation_id"],
        )
        if (
            row["entity_relationship_id"] != machine_id("entity-relation", identity)
            or provenance not in valid_provenance
            or row["source_entity_id"] not in canonical_entities
            or row["target_entity_id"] not in canonical_entities
            or row["temporal_type"] != "interval"
            or row["effective_at"] is not None
            or row["effective_from"] is None
            or (row["effective_to"] is not None and row["effective_from"] >= row["effective_to"])
        ):
            return False
        parent = relationship_by_id.get(row["revision_of"])
        if (row["version"] == 1 and row["revision_of"] is not None) or (
            row["version"] > 1
            and (
                parent is None
                or parent["version"] != row["version"] - 1
                or parent["source_evidence_item_id"] != row["source_evidence_item_id"]
                or parent["relation_type"] != row["relation_type"]
                or parent["source_entity_id"] != row["source_entity_id"]
                or parent["target_entity_id"] != row["target_entity_id"]
            )
        ):
            return False
    return True


def _source_schema_closure_is_valid(conn, manifest: X3FixtureManifest) -> bool:
    schemas = conn.execute(
        "SELECT * FROM payload_schema WHERE payload_schema_id LIKE 'schema:x3-%-source-v1' "
        "ORDER BY payload_schema_id"
    ).fetchall()
    if len(schemas) != len(_DATASET_IDS):
        return False
    for row in schemas:
        schema = json.loads(row["schema_json"])
        if (
            row["schema_sha256"] != sha256(canonical_bytes(schema))
            or manifest.payload_schema_digests.get(row["payload_schema_id"]) != row["schema_sha256"]
        ):
            return False
    sources = conn.execute(
        "SELECT * FROM source_record WHERE dataset_id LIKE 'dataset:x3-%' ORDER BY source_record_id"
    ).fetchall()
    if not sources:
        return False
    for row in sources:
        expected = machine_id(
            "source-record",
            {
                "dataset_id": row["dataset_id"],
                "source_system": row["source_system"],
                "source_record_key": row["source_record_key"],
            },
        )
        if row["source_record_id"] != expected or row["dataset_id"] not in manifest.dataset_ids:
            return False
    items = conn.execute(
        "SELECT i.*,s.dataset_id FROM evidence_item i JOIN source_record s USING(source_record_id) "
        "WHERE s.dataset_id LIKE 'dataset:x3-%' ORDER BY i.evidence_item_id"
    ).fetchall()
    if not items or {row["source_record_id"] for row in items} != {
        row["source_record_id"] for row in sources
    }:
        return False
    for row in items:
        payload = json.loads(row["payload_json"])
        if (
            row["content_sha256"] != sha256(canonical_bytes(payload))
            or row["payload_schema_id"] != _PAYLOAD_SCHEMA_IDS[row["dataset_id"]]
            or row["field_dictionary_version"] != "x3-v1"
            or set(payload) != set(_PAYLOAD_FIELDS[row["dataset_id"]])
            or manifest.field_dictionaries.get(row["dataset_id"])
            != _PAYLOAD_FIELDS[row["dataset_id"]]
        ):
            return False
    return True


def _snapshot_closure_is_valid(conn, manifest: X3FixtureManifest) -> bool:
    for dataset_id in manifest.dataset_ids:
        digest = manifest.slice_digests[dataset_id]
        receipt_id = manifest.slice_receipt_ids[dataset_id]
        stored = conn.execute(
            "SELECT * FROM snapshot_manifest WHERE snapshot_digest=?", (digest,)
        ).fetchone()
        if stored is None or not _persisted_receipt_is_valid(conn, receipt_id):
            return False
        snapshot = as_known_slice(
            conn,
            decision_at=X3_CUTOFFS["latest"],
            request=DatasetSliceRequest(
                dataset_id,
                manifest.access_contexts[dataset_id],
                manifest.right_ids[dataset_id],
                "x3-research",
                valid_at=X3_CUTOFFS["latest"],
            ),
        )
        expected_request = canonical_bytes(
            {"request": snapshot.request, "decision_at": snapshot.decision_at}
        ).decode()
        expected_records = sha256(b"".join(canonical_bytes(row) + b"\n" for row in snapshot.rows))
        if (
            snapshot.digest != digest
            or snapshot.receipt_id != receipt_id
            or stored["request_json"] != expected_request
            or stored["row_count"] != len(snapshot.rows)
            or stored["records_sha256"] != expected_records
        ):
            return False
        verification_bundle = SnapshotBundle(
            SnapshotBundleRequest(
                X3_CUTOFFS["latest"],
                (snapshot.request,),
                ("evidence_item_id",),
                "x3-manifest-verification-v1",
            ),
            (snapshot,),
            "",
            "",
            "",
        )
        verify_receipt(conn, receipt_id, verification_bundle)
    return True


def _composite_closure_is_valid(conn, manifest: X3FixtureManifest) -> bool:
    policies = {
        "x3-source-union-v1": (
            ("canonical_entity_id",),
            manifest.coverage_dataset_ids[:5],
        ),
        "x3-target-cell-v1": (
            ("canonical_entity_id", "target_grid_cell_id"),
            ("dataset:x3-target-grid", "dataset:x3-funnel-event"),
        ),
        "x3-funnel-opportunity-v1": (("opportunity_id",), _FUNNEL_DATASETS),
    }
    if tuple(policies) != manifest.join_policies:
        return False
    for policy, bundle_digest in zip(manifest.join_policies, manifest.bundle_digests, strict=True):
        join_keys, datasets = policies[policy]
        receipt_id = manifest.composite_receipt_ids[policy]
        composite_digest = manifest.composite_digests[policy]
        row = conn.execute(
            "SELECT * FROM snapshot_bundle_manifest WHERE bundle_digest=?", (bundle_digest,)
        ).fetchone()
        header = conn.execute(
            "SELECT * FROM reconstruction_receipt WHERE receipt_id=?", (receipt_id,)
        ).fetchone()
        slice_digests = tuple(
            (dataset_id, manifest.slice_digests[dataset_id]) for dataset_id in datasets
        )
        expected_bundle = digest_id(
            "bundle",
            {
                "policy": policy,
                "join_keys": join_keys,
                "slice_digests": slice_digests,
                "composite_digest": composite_digest,
                "join_receipt_id": receipt_id,
            },
        )
        if (
            row is None
            or header is None
            or not _persisted_receipt_is_valid(conn, receipt_id)
            or bundle_digest != expected_bundle
            or row["request_json"]
            != canonical_bytes({"policy": policy, "join_keys": join_keys}).decode()
            or row["slice_digests_json"] != canonical_bytes(slice_digests).decode()
            or row["composite_input_digest"] != composite_digest
            or row["join_receipt_id"] != receipt_id
            or row["records_sha256"] != header["value_sha256"]
        ):
            return False
    return True


