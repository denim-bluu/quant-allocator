def _add_relationships(
    conn, dataset_sources, items_by_source, span_by_item, versions_by_dataset, observations
) -> tuple[str, ...]:
    dataset_id = _COVERAGE_DATASETS[0]
    latest_version = versions_by_dataset[dataset_id][-1]
    latest_observation_by_item = {
        row.evidence_item_id: row for row in observations[(dataset_id, len(_VERSION_LABELS) - 1)]
    }
    early_version = versions_by_dataset[dataset_id][0]
    early_observation_by_item = {row.evidence_item_id: row for row in observations[(dataset_id, 0)]}
    evidence_rows = [
        row
        for row in dataset_sources[dataset_id]
        if not row.source_record_key.endswith("-manifest")
    ]
    specs: list[tuple[str, str, str]] = []
    for index in range(8):
        specs.extend(
            (
                ("advised_by", f"manager:x3-{index:02d}", f"adviser:x3-{index:02d}"),
                ("legal_identity", f"adviser:x3-{index:02d}", f"legal-entity:x3-{index:02d}"),
            )
        )
    for index in range(24):
        specs.extend(
            (
                ("offers", f"manager:x3-{index // 3:02d}", f"strategy:x3-{index:02d}"),
                ("reported_through", f"strategy:x3-{index:02d}", f"composite:x3-{index:02d}"),
                ("implemented_by", f"composite:x3-{index:02d}", f"vehicle:x3-{index:02d}"),
            )
        )
    identifiers = []

    def insert_relation(
        *,
        item,
        span,
        version,
        observation,
        relation_type,
        source_entity,
        target_entity,
        effective_from,
        effective_to,
        relation_version,
        revision_of,
    ) -> str:
        identity = {
            "source_evidence_item_id": item.evidence_item_id,
            "relation_type": relation_type,
            "source_entity_id": source_entity,
            "target_entity_id": target_entity,
            "temporal_type": "interval",
            "effective_at": None,
            "effective_from": effective_from,
            "effective_to": effective_to,
            "version": relation_version,
        }
        identifier = machine_id("entity-relation", identity)
        _insert(
            conn,
            "entity_relationship",
            (
                "entity_relationship_id",
                "source_evidence_item_id",
                "evidence_span_id",
                "dataset_version_id",
                "dataset_observation_id",
                "relation_type",
                "source_entity_id",
                "target_entity_id",
                "temporal_type",
                "effective_at",
                "effective_from",
                "effective_to",
                "version",
                "revision_of",
            ),
            (
                identifier,
                item.evidence_item_id,
                span.evidence_span_id,
                version.dataset_version_id,
                observation.dataset_observation_id,
                relation_type,
                source_entity,
                target_entity,
                "interval",
                None,
                effective_from,
                effective_to,
                relation_version,
                revision_of,
            ),
        )
        identifiers.append(identifier)
        return identifier

    for ordinal, (relation_type, source_entity, target_entity) in enumerate(specs):
        source = evidence_rows[ordinal]
        item = next(
            candidate
            for candidate in items_by_source[source.source_record_id]
            if candidate.evidence_item_id in latest_observation_by_item
        )
        span = span_by_item[item.evidence_item_id]
        if relation_type == "advised_by" and source_entity == "manager:x3-00":
            early_observation = early_observation_by_item[item.evidence_item_id]
            base_id = insert_relation(
                item=item,
                span=span,
                version=early_version,
                observation=early_observation,
                relation_type=relation_type,
                source_entity=source_entity,
                target_entity=target_entity,
                effective_from="2024-01-01T00:00:00.000000Z",
                effective_to=None,
                relation_version=1,
                revision_of=None,
            )
            insert_relation(
                item=item,
                span=span,
                version=latest_version,
                observation=latest_observation_by_item[item.evidence_item_id],
                relation_type=relation_type,
                source_entity=source_entity,
                target_entity=target_entity,
                effective_from="2024-01-01T00:00:00.000000Z",
                effective_to="2024-07-01T00:00:00.000000Z",
                relation_version=2,
                revision_of=base_id,
            )
            insert_relation(
                item=item,
                span=span,
                version=latest_version,
                observation=latest_observation_by_item[item.evidence_item_id],
                relation_type=relation_type,
                source_entity=source_entity,
                target_entity="adviser:x3-01",
                effective_from="2024-07-01T00:00:00.000000Z",
                effective_to=None,
                relation_version=1,
                revision_of=None,
            )
            continue
        insert_relation(
            item=item,
            span=span,
            version=latest_version,
            observation=latest_observation_by_item[item.evidence_item_id],
            relation_type=relation_type,
            source_entity=source_entity,
            target_entity=target_entity,
            effective_from="2024-01-01T00:00:00.000000Z",
            effective_to=None,
            relation_version=1,
            revision_of=None,
        )
    return tuple(sorted(identifiers))


