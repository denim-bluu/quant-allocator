def _source_shape() -> dict[str, tuple[str, str, str]]:
    """Return live-shaped labels and reviewed mappings, never hidden truth labels."""

    out: dict[str, tuple[str, str, str]] = {}
    for index in range(840):
        dataset_id = _COVERAGE_DATASETS[index % 5]
        ordinal = index % 24
        if dataset_id == "dataset:x3-public-adviser":
            entity_type, ordinal = "manager", index % 8
        elif dataset_id in {"dataset:x3-registered-fund", "dataset:x3-holdings-filer"}:
            entity_type = "vehicle"
        elif dataset_id == "dataset:x3-strategy-export":
            entity_type = "composite"
        else:
            entity_type, ordinal = "strategy", (index // 5) % 24
        out[f"x3-source-{index:04d}"] = (
            dataset_id,
            f"Quill {entity_type.title()} {ordinal + 1:02d}",
            f"{entity_type}:x3-{ordinal:02d}",
        )
    return out


